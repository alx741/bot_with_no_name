void select_eyes(EYE_t eye_left, EYE_t eye_right)
{
    void *fb_ptr = &FRAME_BUFFER;

    for (int i = 0; i < 8; i++)
    {
        memset(fb_ptr++, vec2byte(eye_right[i]), 1);
        memset(fb_ptr++, vec2byte(eye_left[i]), 1);
    }
}

